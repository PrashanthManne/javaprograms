package learnif;

public class switchwithdefault {

	public static void main(String[] args) {
	String s = "sweets";
	switch(s)
	{
	case "fruit" : System.out.println("fruit has been  choosed ");
	break ;
	case "flower" : System.out.println("flower has been  choosed ");
	break ;
	case "veg" : System.out.println("veg has been  choosed ");
	break ;
	case "sweets" : System.out.println("sweets has been  choosed ");
	break;

	default : System.out.println("Nothing has been  choosed ");
	
	}

	}

}
